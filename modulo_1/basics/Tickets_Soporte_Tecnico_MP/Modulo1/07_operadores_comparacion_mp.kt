fun main() {
    println("operadores de comparacion")
    val numero1=10
    var numero2=2
    
    println("$numero1 == $numero2 : ${numero1==numero2}")
    println("$numero1 != $numero2 : ${numero1!=numero2}")
    println("$numero1 > $numero2: ${numero1>numero2}")
    println("$numero1 < $numero2 : ${numero1<numero2}")
    println("$numero1 <= $numero2 : ${numero1<=numero2}")
    println("$numero1 >= $numero2 : ${numero1>=numero2}")
}